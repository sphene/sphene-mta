getVehicleEntryPoints = function(vehicle)
    local vehicleModel = vehicle:getModel()

    if (_getVehicleEntryPoints and type(vehicle) ~= 'table') then
        local entryPoints = _getVehicleEntryPoints(vehicle)

        if (entryPoints) then
            return entryPoints
        end
    end

    local entryOffsets = vehicleEntryOffsets[vehicleModel - 399] or false

    if (entryOffsets) then
        local vehicleMatrix = vehicle:getMatrix()

        local entryPoints = {}

        for index, entryPoint in ipairs(entryOffsets) do
            local vector = vehicleMatrix:transformPosition(entryPoint[1], entryPoint[2], entryPoint[3])

            entryPoints[index] = {vector:getX(), vector:getY(), vector:getZ()}
        end

        return entryPoints
    end

    return false
end

